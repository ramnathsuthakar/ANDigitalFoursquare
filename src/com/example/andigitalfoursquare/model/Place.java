package com.example.andigitalfoursquare.model;

public class Place {
	
	String name;
	String iconURL;

	public Place() {}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIconURL() {
		return iconURL;
	}

	public void setIconURL(String iconURL) {
		this.iconURL = iconURL;
	}
	
	
	

}
